package Day1Assignments;

public class PrimitiveData {
    static int k;
    static float f;
    static String p;
    static double d;

    public static void main(String[] args) {
        System.out.println("int default value = " + k);
        System.out.println("float default value = " + f);
        System.out.println("double default value = " + d);
        boolean istrue = false;
        System.out.println("default value of boolean = " + istrue);
        System.out.println("default value of String = " + p);
    }
}